def normalize(text):
    if not text:
        return ''
    return re.sub(r'\s+', ' ', text.strip().lower())


# sub_genre ← origin mapping (only high-confidence geographic sub_genres)
SUBGENRE_ORIGINS = {
    'ספרות ישראלית עכשווית': 'ישראלית',
    'ספרות עברית קלאסית':    'ישראלית',
    'ספרות לטינו-אמריקאית':  'לטינו-אמריקאית',
    'ספרות יפנית':            'יפנית',
    'ספרות סקנדינבית':        'סקנדינבית',
    'ספרות ספרדית':           'ספרדית',
    'ספרות רוסית':            'רוסית',
    'ספרות גרמנית':           'גרמנית',
    'ספרות צרפתית':           'צרפתית',
    'ספרות איטלקית':          'איטלקית',
    'ספרות פורטוגזית':        'פורטוגזית',
    'ספרות ערבית':            'ערבית',
    'ספרות אמריקאית':         'אמריקאית',
    'ספרות בריטית':           'בריטית',
}


def detect_origin(book):
    """מזהה ארץ מוצא לספר אחד. מחזיר (origin, method)."""
    author    = normalize(book.get('author') or '')
    sub_genre = book.get('sub_genre') or ''

    # (1) בדיקה בטבלת מחברים — מחרוזת חלקית (עדיפות גבוהה)
    for key, origin in AUTHOR_ORIGINS.items():
        if normalize(key) in author:
            return origin, 'lookup'

    # (2) sub_genre → origin (עדיפות בינונית)
    if sub_genre in SUBGENRE_ORIGINS:
        return SUBGENRE_ORIGINS[sub_genre], 'sub_genre'

    # (3) לא זוהה
    return None, 'unknown'


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--dry-run', action='store_true', help='הצג בלבד, אל תשמור')
    parser.add_argument('--unknown', action='store_true', help='הדפס מחברים שלא זוהו')
    args = parser.parse_args()

    with open(LIB_PATH, encoding='utf-8-sig') as f:
        content = f.read()
    m = re.search(r'var LOCAL_LIBRARY\s*=\s*(\[.*\])\s*;', content, re.DOTALL)
    books = json.loads(m.group(1))
    prefix = content[:m.start(1)]
    suffix = content[m.end(1):]

    from collections import Counter
    origin_counts  = Counter()
    method_counts  = Counter()
    unknown_authors = {}

    for book in books:
        # נקה ערך ישן לפני זיהוי מחדש
        book.pop('origin', None)
        origin, method = detect_origin(book)
        if origin:
            book['origin'] = origin
            origin_counts[origin] += 1
            method_counts[method] += 1
        else:
            author = (book.get('author') or '').strip()
            if author:
                unknown_authors[author] = unknown_authors.get(author, 0) + 1

    total = len(books)
    covered = sum(origin_counts.values())
    print(f'\n=== תוצאות ===')
    print(f'כיסוי: {covered}/{total} ({100*covered//total}%)')
    print(f'\nשיטת זיהוי:')
    for method, cnt in method_counts.most_common():
        print(f'  {cnt:4d}  {method}')
    print(f'\nהתפלגות ארצות מוצא:')
    for orig, cnt in origin_counts.most_common():
        bar = '█' * (cnt // 10)
        print(f'  {cnt:4d}  {orig:25}  {bar}')

    if args.unknown:
        print(f'\n=== {len(unknown_authors)} מחברים לא מזוהים (מוצגים לפי שכיחות) ===')
        for author, cnt in sorted(unknown_authors.items(), key=lambda x: -x[1])[:60]:
            print(f'  {cnt:3d}x  {author}')

    if not args.dry_run:
        with open(LIB_PATH, 'w', encoding='utf-8') as f:
            f.write(prefix)
            f.write(json.dumps(books, ensure_ascii=False, indent=2))
            f.write(suffix)
        print(f'\n✓ נשמר: {LIB_PATH}')
    else:
        print('\n(dry-run — לא נשמר)')


if __name__ == '__main__':
    main()
